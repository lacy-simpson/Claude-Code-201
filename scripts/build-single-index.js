#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const shellPath = path.join(root, "index_v2.html");
const cssPath = path.join(root, "assets", "css", "styles.css");
const jsPath = path.join(root, "assets", "js", "app.js");
const outputPath = path.join(root, "index.html");

function readText(relativeOrAbsolutePath) {
  return fs.readFileSync(relativeOrAbsolutePath, "utf8");
}

function escapeClosingTag(source, tagName) {
  return source.replace(new RegExp("</" + tagName, "gi"), "<\\/" + tagName);
}

function mimeTypeFor(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  if (ext === ".png") return "image/png";
  if (ext === ".jpg" || ext === ".jpeg") return "image/jpeg";
  if (ext === ".gif") return "image/gif";
  if (ext === ".webp") return "image/webp";
  if (ext === ".svg") return "image/svg+xml";
  throw new Error("Unsupported image type: " + filePath);
}

const assetCache = new Map();
function toDataUri(relativePath) {
  if (assetCache.has(relativePath)) return assetCache.get(relativePath);

  const absolutePath = path.join(root, relativePath);
  const data = fs.readFileSync(absolutePath).toString("base64");
  const uri = "data:" + mimeTypeFor(absolutePath) + ";base64," + data;
  assetCache.set(relativePath, uri);
  return uri;
}

function inlineLocalImageSources(markup) {
  return markup.replace(/(src=["'])(assets\/imgs\/[^"']+)(["'])/g, function(match, prefix, assetPath, suffix) {
    return prefix + toDataUri(assetPath) + suffix;
  });
}

function extractLessonFiles(appJs) {
  const lessonFiles = [];
  const seen = new Set();
  const re = /file:"([^"]+)"/g;
  let match;

  while ((match = re.exec(appJs)) !== null) {
    const file = match[1];
    if (!seen.has(file)) {
      seen.add(file);
      lessonFiles.push(file);
    }
  }

  if (lessonFiles.length === 0) {
    throw new Error("Could not find lesson files in assets/js/app.js");
  }

  return lessonFiles;
}

function makeStandaloneAppJs(appJs) {
  const loaderStart = appJs.indexOf("  function renderLessonError(message){");
  const initStart = appJs.indexOf("  function init(){");

  if (loaderStart === -1 || initStart === -1 || initStart <= loaderStart) {
    throw new Error("Could not find the lesson loader block in assets/js/app.js");
  }

  const standaloneLoader = [
    'document.addEventListener("DOMContentLoaded", function(){',
    "    init();",
    "  });",
    ""
  ].join("\n");

  return (
    appJs.slice(0, loaderStart) +
    "  " + standaloneLoader +
    appJs.slice(initStart)
  ).replace(/, file:"[^"]+"/g, "");
}

function build() {
  const shell = readText(shellPath);
  const css = readText(cssPath);
  const appJs = readText(jsPath);
  const lessonFiles = extractLessonFiles(appJs);
  const lessons = lessonFiles.map(function(file) {
    const lessonPath = path.join(root, file);
    return inlineLocalImageSources(readText(lessonPath).trim());
  }).join("\n\n");

  const standaloneJs = makeStandaloneAppJs(appJs);

  let html = shell
    .replace(
      /<link rel="stylesheet" href="assets\/css\/styles\.css\?v=\d+">/,
      "<style>\n" + escapeClosingTag(css, "style") + "\n</style>"
    )
    .replace(
      /<div class="pages" id="pages">[\s\S]*?<\/div><!-- \/\.pages -->/,
      '<div class="pages" id="pages">\n\n' + lessons + '\n\n      </div><!-- /.pages -->'
    )
    .replace(
      /<script src="assets\/js\/app\.js\?v=\d+" defer><\/script>/,
      "<script>\n" + escapeClosingTag(standaloneJs, "script") + "\n</script>"
    );

  if (html === shell) {
    throw new Error("No replacements were made. Check index_v2.html structure.");
  }

  const banner = [
    "<!--",
    "  Generated by scripts/build-single-index.js.",
    "  Edit index_v2.html, assets/css/styles.css, assets/js/app.js, or lessons/*.html, then rerun the script.",
    "-->"
  ].join("\n");

  html = html.replace("<!DOCTYPE html>", "<!DOCTYPE html>\n" + banner);

  fs.writeFileSync(outputPath, html, "utf8");

  console.log("Built " + path.relative(root, outputPath));
  console.log("Inlined " + lessonFiles.length + " lessons");
  console.log("Embedded " + assetCache.size + " image assets");
}

try {
  build();
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
